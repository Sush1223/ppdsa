#include <bits/stdc++.h>
using namespace std;

int main()
{
	int n ;
	cin >>n;
	switch (n%7)
	{
		case 1: cout<<"m";
		break ;

		case 2: cout<<"t";
		break ;

		case 3: cout<<"w";
		break ;

		case 4: cout<<"thr";
		break ;

		case 5: cout<<"fri";
		break ;

		case 6: cout<<"sat";
		break ;

		default: cout<<"sun";
		
	
}
}
